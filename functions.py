# soma
def soma(a,b):
    return a + b

# subtracao
def subtracao(a,b):
    return a - b

# multiplicacao
def multiplicao(a,b):
    return a * b

# divisao
def divisao(a,b):
    if (b == 0):
        print("Não existe divisão por zero. \n")
    else:
        return a/b

# potenciação

def potenciacao(a,b):
    return a**b

# raiz quadrada
def raiz_quadrada(numero):
    return numero**0.5 
